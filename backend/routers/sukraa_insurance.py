def map_insurance_name_to_code(name: str):
    """Fuzzy mapping from extracted Referrer Name to its ID code."""
    if not name: return None
    name_clean = name.lower().split('|')[0].strip()
    
    for code, scheme in INSURANCE_SCHEMES.items():
        scheme_clean = scheme.lower().split('|')[0].strip()
        if name_clean in scheme_clean or scheme_clean in name_clean:
            return code
            
    # Fallback to loose matching
    for code, scheme in INSURANCE_SCHEMES.items():
        scheme_clean = scheme.lower().split('|')[0].strip()
        words = set(name_clean.split())
        words_target = set(scheme_clean.split())
        if len(words) > 1 and len(words.intersection(words_target)) >= max(1, len(words) - 1):
            return code
            
    return None
